def compute_metrics(df, name):
    return {
        "Algorithm": name,
        "Success Rate": df['success'].mean(),
        "Avg Path Length": df[df['path_length'] > 0]['path_length'].mean(),
        "Avg Explored Nodes": df['explored_nodes'].mean()
    }

