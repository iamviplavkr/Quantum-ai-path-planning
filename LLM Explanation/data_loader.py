def format_llm_text(text):
    if not text:
        return ""

    text = re.sub(r"\*\*(.*?)\*\*", r"<b>\1</b>", text)
    text = re.sub(r"## (.*?)\n", r"<b>\1</b><br/><br/>", text)
    text = text.replace("* ", "• ")
    text = text.replace("\n", "<br/>")
    return text

